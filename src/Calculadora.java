// Metodo para el boton igual
    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        this.numDos = Float.parseFloat(this.casilla.getText());
        
        switch (this.operador) {
            case "+":
                this.casilla.setText(sacadoElCero(this.numUno + this.numDos));
                break;
            case "-":
                this.casilla.setText(sacadoElCero(this.numUno - this.numDos));
                break;
            case "*":
                this.casilla.setText(sacadoElCero(this.numUno * this.numDos));
                break;
            case "/":
                if (this.numDos==0){
                    this.casilla.setText(" No se puede dividir entre 0!!");
                }
                else{
                this.casilla.setText(sacadoElCero(this.numUno / this.numDos));
                break;
                }
        }
    }//GEN-LAST:event_jButton16ActionPerformed

